    private void unlockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unlockActionPerformed
        action = evt.getActionCommand();
        if(evt.getActionCommand().equals("Click Here to Unlock The Settings"))
        {
            verifypasswordframe obj = new verifypasswordframe();
            useraccount = true;
            obj.setVisible(true);
            obj.idnumber=idnumber;
        }
        else
        {
            unlock.setText("Click Here to Unlock The Settings");
            email.disable();
            cellphone.disable();
            flagvar.setText("false");
                try
                {
                    Connection myconnection = DriverManager.getConnection(PATH+PLACE, USERNAME, PASSWORD);
                    try
                    {
                        String qu = "update users set userEmail=?,phoneNo=? where id=?";
                        PreparedStatement mystatement = myconnection.prepareStatement(qu);
                        mystatement.setString(1,email.getText());
                        mystatement.setString(2,cellphone.getText());
                        mystatement.setString(3,idnumber);
                        if(mystatement.executeUpdate()>0)
                        {
                            JOptionPane.showMessageDialog(this, "Success");
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "Error in Current Password..");
                        }
                    }
                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null, "Error Due to : "+e.getMessage());
                    }
                    finally
                    {
                        myconnection.close();
                    }
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, "Error Due to : "+e.getMessage());
                }
            
        }
    }//GEN-LAST:event_unlockActionPerformed

