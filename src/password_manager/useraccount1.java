    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            if(newpassword.getText().equals(confirmpassword.getText()))
            {
                try
                {
                    Connection myconnection = DriverManager.getConnection(PATH+PLACE, USERNAME, PASSWORD);
                    try
                    {
                        String qu = "select * from users where id=? and userpass=?";
                        PreparedStatement mystatement = myconnection.prepareStatement(qu);
                        mystatement.setString(1,idnumber);
                        mystatement.setString(2,currentpassword.getText());
                        ResultSet result = mystatement.executeQuery();
                        if(result.next())
                        {
                            String query = "update users set userpass=? where id=?";
                            PreparedStatement obj = myconnection.prepareStatement(query);
                            obj.setString(1,newpassword.getText());
                            obj.setString(2,idnumber);
                            if(obj.executeUpdate()>0)
                            {
                                JOptionPane.showMessageDialog(this,"Changes Has Been Saved..");
                                currentpassword.setText("");
                                newpassword.setText("");
                                confirmpassword.setText("");
                            }
                            else
                            {
                                JOptionPane.showMessageDialog(this,"Error in Saving the New Password...");
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "Error in Current Password..");
                        }
                    }
                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null, "Error Due to : "+e.getMessage());
                    }
                    finally
                    {
                        myconnection.close();
                    }
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, "Error Due to : "+e.getMessage());
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Password Doesn't Match");
            }
    }//GEN-LAST:event_jButton2ActionPerformed

